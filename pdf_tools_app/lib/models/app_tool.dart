// Get only P0 tools for MVP
List<AppTool> get p0Tools => allTools.where((tool) => tool.priority == ToolPriority.p0).toList();

// Get tools by category
List<AppTool> getToolsByCategory(ToolCategory category) {
  return p0Tools.where((tool) => tool.category == category).toList();
}

// Get tool by ID
AppTool? getToolById(String id) {
  try {
    return allTools.firstWhere((tool) => tool.id == id);
  } catch (e) {
    return null;
  }
}

// Search tools
List<AppTool> searchTools(String query) {
  final lowerQuery = query.toLowerCase();
  return p0Tools.where((tool) {
    return tool.name.toLowerCase().contains(lowerQuery) ||
        tool.description.toLowerCase().contains(lowerQuery) ||
        tool.shortDescription.toLowerCase().contains(lowerQuery);
  }).toList();
}
