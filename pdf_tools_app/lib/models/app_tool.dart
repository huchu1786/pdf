import 'package:flutter/material.dart';
import '../theme/app_theme.dart';

enum ToolCategory {
  pdfTools,
  imageTools,
  calculatorTools,
  otherTools,
}

enum ProcessingMode {
  local, // On-device processing
  api, // Backend API processing
}

enum ToolPriority {
  p0, // MVP
  p1, // Version 1.1
  p2, // Version 1.2+
}

extension ToolCategoryExtension on ToolCategory {
  String get displayName {
    switch (this) {
      case ToolCategory.pdfTools:
        return 'PDF Tools';
      case ToolCategory.imageTools:
        return 'Image Tools';
      case ToolCategory.calculatorTools:
        return 'Calculators';
      case ToolCategory.otherTools:
        return 'Other Tools';
    }
  }

  Color get color {
    switch (this) {
      case ToolCategory.pdfTools:
        return AppColors.pdfCategory;
      case ToolCategory.imageTools:
        return AppColors.imageCategory;
      case ToolCategory.calculatorTools:
        return AppColors.calculatorCategory;
      case ToolCategory.otherTools:
        return AppColors.otherCategory;
    }
  }

  IconData get icon {
    switch (this) {
      case ToolCategory.pdfTools:
        return Icons.picture_as_pdf;
      case ToolCategory.imageTools:
        return Icons.image;
      case ToolCategory.calculatorTools:
        return Icons.calculate;
      case ToolCategory.otherTools:
        return Icons.build;
    }
  }
}

class AppTool {
  final String id;
  final String name;
  final String description;
  final String shortDescription;
  final ToolCategory category;
  final ProcessingMode processingMode;
  final ToolPriority priority;
  final IconData icon;
  final List<String> supportedInputFormats;
  final List<String> supportedOutputFormats;
  final List<String> instructions;
  final List<String> features;
  final bool supportsMultipleFiles;
  final int maxFileSizeMB;
  final int maxFilesCount;

  const AppTool({
    required this.id,
    required this.name,
    required this.description,
    required this.shortDescription,
    required this.category,
    required this.processingMode,
    required this.priority,
    required this.icon,
    required this.supportedInputFormats,
    required this.supportedOutputFormats,
    required this.instructions,
    this.features = const [],
    this.supportsMultipleFiles = false,
    this.maxFileSizeMB = 25,
    this.maxFilesCount = 5,
  });
}

